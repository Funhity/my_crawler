    /**
     * 随机生成一个男性姓名
     */
    public function getNameMale() {
        
        $last_name_list = $this->getLastNameList();
        
        $first_name_list = $this->getMaleFirstNameList();
        
        
        $last_name = $last_name_list[$this->getRandomInt(0, count($last_name_list)-1)];
        
        $first_name = $first_name_list[$this->getRandomInt(0, count($first_name_list)-1)];
        
        
        return $first_name.' '.$last_name;
    }
    
    
    
    
    public function getRandomInt($start, $end) {
        return rand($start,$end);
    }
    
